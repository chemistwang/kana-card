// 获取所有假名字符的平坦数组
export const getAllCharacters = () => {
  return kanaData.flatMap(row => row.characters);
};

// 根据练习模式获取字符
export const getCharactersByMode = (mode: 'hiragana' | 'katakana' | 'mixed') => {
  const allCharacters = getAllCharacters();
  
  if (mode === 'mixed') {
    return allCharacters;
  }
  
  return allCharacters;
};

// 随机获取一个字符
export const getRandomCharacter = (mode: 'hiragana' | 'katakana' | 'mixed') => {
  const characters = getCharactersByMode(mode);
  const randomIndex = Math.floor(Math.random() * characters.length);
  return characters[randomIndex];
};